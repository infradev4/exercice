""" Les assignations sont utilisées pour lier des noms à des valeurs
a = 1
Les assignations augmentées ou (reassignation) permettent de simplifier l'ecriture

Le symbole **= va mettre la variable a à la puissance 2, ce qui donne donc 25.
    a = 5
    a **= 2

Attention:
 Si l'on enlève tous les espaces on se rend compte que cette syntaxe est possible. En fait Python va considérer le + comme le symbole 'positif' et a sera donc égal à +1 (donc 1). 
 Le symbole = sera donc dans ce cas un symbole d'assignation classique comme lorsqu'on assigne une valeur à une variable.:
    a = 5
    a =+ 1
"""

#Est-ce que A est Plus petit ou egale B (<=)
a = 1
a = a + 5
print(a)

# Ou bien je peux raccourcir avec +=

a = 1
a += 5
